a = 34

b = 2
# Arithmetic Operator
print("a + b = ", a + b)
print("a - b = ", a - b)
print("a * b = ", a * b)
print("a / b = ", a / b)
print("a % b = ", a % b)
print("a // b = ", a // b)
print("a ** b = ", a ** b)

# Conditional Operators
print(a>4)
print(a<4)
print(a<=4)
print(a>=4)
print(a==4)
print(a==34)
print(a!=34)


# Logical Opeartors

c = True
d = False
print(c and c)
print(c and d)
print(d and c)

print("For Or operator...")
print(c or c)
print(c or d)
print(d or c)

print("Not operator")
print(not(True))
print(not(False))


print("Assignment operator")
a = 32
print(a)
a*=3
print(a)
